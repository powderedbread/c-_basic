#include <stdio.h>   
#include <cmath>
#define PI 3.14159265358979323846

// radians = (pi * degrees)/180
// sin function uses radians

int main(void) {
    double degrees;
    double radians;
    double result;

    printf("Find the sin of the input \nInput a degree value: ");
    scanf_s("%lf", &degrees);

    radians = (degrees * PI) / 180;
    result = sin(radians);

    printf("The sign of %lf degrees is %lf", degrees, result);
    return 0;
 }  
